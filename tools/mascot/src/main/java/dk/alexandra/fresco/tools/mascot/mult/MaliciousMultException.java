package dk.alexandra.fresco.tools.mascot.mult;

public class MaliciousMultException extends Exception {

  /**
   * 
   */
  private static final long serialVersionUID = 3756912425763637515L;

  public MaliciousMultException() {
    super();
  }

  public MaliciousMultException(String message) {
    super(message);
  }

  public MaliciousMultException(String message, Throwable cause) {
    super(message, cause);
  }

  public MaliciousMultException(Throwable cause) {
    super(cause);
  }

}
